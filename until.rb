# The complement to the while loop is the until loop.
#  It's sort of like a backward while:

counter = 1
until counter > 10
  puts counter
  # Add code to update 'counter' here!
  counter += 1
end

# In the example above, we first create a variable called counter
# and set it to 1.

# Then we execute a block of code until counter is greater than 10.
#That block of code increments counter

# When counter is equal to or greater than 10, the block ends.
